namespace Eduard
{
    /// <summary>
    /// Specifies the numerical base for integer representation and conversion.
    /// </summary>
    public enum Radix : int
    {
        /// <summary>
        /// Base-10 decimal representation.
        /// </summary>
        Decimal = 10,

        /// <summary>
        /// Base-16 hexadecimal representation.
        /// </summary>
        HexaDecimal = 16
    }
}
